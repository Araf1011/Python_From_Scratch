'''Snake case
where all the letters are in lowercase and words are separated by underscores.'''

my_name = "Araf"
my_age = 23
my_height = 5.0
is_student = True


"""
Camel case
where the first letter of each word is capitalized except for the first word, and there are no spaces or underscores."""
myName = "Araf"
myAge = 23
myHeight = 5.0
isStudent = True

'''Pascal case
where the first letter of each word is capitalized, and there are no spaces or underscores.'''
MyName = "Araf"
MyAge = 23
MyHeight = 5.0
IsStudent = True