#Convert from int to float
a = 5
b = float(a)
print(b) #output: 5.0

#Convert from float to int
c = 3.14
d = int(c)
print(d) #output: 3

#Convert from int to string
e = 42
f = str(e)
print(f) #output: '42'

#Convert to boolean
g = 0
h = bool(g)
print(h) #output: False
i = 1
j = bool(i)
print(j) #output: True

